import UIKit

extension UIColor {
    static var purple: UIColor {
        return UIColor(named: "Purple") ?? UIColor()
    }
    
    static var violet: UIColor {
        return UIColor(named: "Violet") ?? UIColor()
    }
    
    static var placeholder: UIColor {
        return UIColor(named: "PlaceholderColor") ?? UIColor()
    }
    
    static var baseText: UIColor {
        return UIColor.black
    }
}
